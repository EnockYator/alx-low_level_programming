#include "function_pointers.h"
#include "calc.h"

/**
*op_add - returns the sum
*@a: first integer
*@b: second integer
*Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - returns subtraction
*@a: first integer
*@b: second integer
*Return: subtraction
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul - returns multiplication
*@a: fisrt integer
*@b: second integer
*Return: multiplication
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
*op_div - returns division
*@a: first integer
*@b: second integer
*Return: division
*/

int op_div(int a, int b);
{
	return (a / b);
}

/**
*op_mod - returns remainder
*@a: first integer
*@b: second integer
*Return: remainder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
